                                                    along with more than a dozen other major contenders. He suffered a punctured lung, fractured ribs and fractured shoulder blade. He spent several days in 
                                                    hospital and then time at his home in Belgium, initially unable to fly due to his injuries. 
                                                </p>
                                            </blockquote>
                                            <p>
                                                His QuickStep-AlphaVinyl team revealed on Thursday that Alaphilippe had undergone further examinations a hospital in Herentals, 
                                                Belgium, which confirmed that his punctured lung had now completely healed, giving him the green light to get back in the saddle.  
                                            </p>
                                            <p>
                                                The world champion was always going to
                                                 take a break from racing after the Ardennes Classics but doubts remain if he will be on form for the Tour de France.According to his QuickStep-AlphaVinyl team manager Patrick Lefevere, he is facing a race against time. 

